import numpy as np
import pandas as pd 
import sklearn.datasets
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

# loading the data from sklearn
breast_cancer_dataset=sklearn.datasets.load_breast_cancer()

# loading the data to a data frame
data_frame=pd.DataFrame(breast_cancer_dataset.data,columns=breast_cancer_dataset.feature_names)

# adding the "target" column to the data frame
data_frame['diagnosis']=breast_cancer_dataset.target


# Separating features and target

X= data_frame.drop(columns='diagnosis',axis=1)
Y= data_frame['diagnosis']

# Splitting data into training and testing data, I made 4 numpy arrays and used the train_test_split function 80% data was used for training while 20% used for testing.

X_train,X_test,Y_train,Y_test= train_test_split(X,Y,test_size=0.2,random_state=2)

# Logistic Regression

model = LogisticRegression()

# training the logistic regression model

model.fit(X_train,Y_train)